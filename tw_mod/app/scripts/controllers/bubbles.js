'use strict';

twModApp.controller('BubblesCtrl', function($scope, $rootScope, $timeout) {
  $rootScope.bodyClass = 'bubbles';




var opts = {
  lines: 2, // The number of lines to draw
  angle: 1, // The length of each line
  lineWidth: 0.1, // The line thickness
  pointer: {
    length: 0.1, // The radius of the inner circle
    strokeWidth: 0.035, // The rotation offset
    color: '#000000' // Fill color
  },
  colorStart: '#fff',   // Colors
  colorStop: '#ddd',   // just experiment with them
  strokeColor: '#DB1853',   // to see which ones work best for you
  generateGradient: true
};
var target = document.getElementById('progress-timer'); // your canvas element
var gauge = new Donut(target).setOptions(opts); // create sexy gauge!
gauge.maxValue = 500; // set max gauge value
gauge.animationSpeed = 500; // set animation speed (32 is default value)
gauge.set(500); // set actual value

//


$timeout(function(){
  $('.bubble').fadeIn('slow');
}, 500);

$timeout(function(){
  $('.bubble').fadeOut('slow');
}, 10000);


});
